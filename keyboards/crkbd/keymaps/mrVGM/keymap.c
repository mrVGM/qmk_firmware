#define KEYBOARD_SIDE 0

static int selected_side = 0;
static int selected_display_mode = 0;
static char kpres = 0;
static uint16_t kpresc = 0;

enum my_keycodes {
    CHANGE_SELECTED_SIDE = SAFE_RANGE,
    CHANGE_SELECTED_MODE
};

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    if (record->event.pressed)
    {
        kpres = 1;
        kpresc = keycode;
    }
  switch (keycode) {
    case CHANGE_SELECTED_SIDE:
      if (record->event.pressed) {
          selected_side += 1;
          selected_side %= 2;
      }
      return false; // Skip all further processing of this key
    case CHANGE_SELECTED_MODE:
      if (record->event.pressed) {
          if (selected_side == KEYBOARD_SIDE)
          {
              selected_display_mode += 1;
          }
      }
      return false; // Skip all further processing of this key
    default:
      return true; // Process all other keycodes normally
  }
}

