uint32_t get_elapsed_ms(void)
{
    static uint32_t elapsed = 0;
    static uint32_t timer = 0;

    uint32_t elps = timer_elapsed(timer);

    if (elps > 333)
    {
        timer = timer_read();
        elapsed += elps;
        elps = 0;
    }
    return (elapsed + elps);
}

