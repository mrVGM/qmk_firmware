typedef void (*draw_func)(uint32_t elapsed);

void draw_blank(uint32_t elapsed)
{
    oled_clear();
}

void draw_squares(uint8_t row, uint8_t l, uint8_t r)
{
    char tmp[32];
    for (int i = 0; i < 16; ++i)
    {
        tmp[i] = l ? 255 : 0;
    }
    for (int i = 16; i < 32; ++i)
    {
        tmp[i] = r ? 255 : 0;
    }

    oled_set_cursor(0, row);
    oled_write_raw(tmp, 32);
    oled_set_cursor(0, row+1);
    oled_write_raw(tmp, 32);
}

void draw_checkers(uint32_t elapsed)
{
    static uint32_t sss;
    if (kpres)
    {
        kpres = 0;
        sss = (elapsed *  kpresc) % 65536;
    }
    oled_clear();
    uint16_t scheme = sss;

    for (int i = 0; i < 8; ++i)
    {
        int cur = scheme % 4;
        scheme /= 4;

        draw_squares(i * 2, cur / 2, cur % 2);
    }
}

bool oled_task_user()
{
    draw_func draw_funcs[] = { draw_pineapple, draw_checkers, draw_blank };
    uint32_t elapsed = get_elapsed_ms();
    draw_funcs[selected_display_mode % 3](elapsed);

    return false;
}

