void print_digit(char* canvas, unsigned digit, unsigned row, unsigned col)
{
    row /= 8;
    const char* dig = digits + digit * 16 * 5;

    for (int i = 0; i < 5; ++i)
    {
        char* ptr = canvas + (row + i) * 32 + col;
        const char* d = dig + 16 * i;
        memcpy_P(ptr, d, 16);
    }
}

void draw_elapsed(uint32_t elapsed_ms)
{
    uint32_t elapsed_secs = elapsed_ms / 1000;
    static char tmp[512];
    memset(tmp, 0, sizeof(tmp));
    uint32_t elapsed = elapsed_secs;
    int secs = elapsed % 60;
    elapsed -= secs;
    elapsed /= 60;

    int mins = elapsed % 60;
    elapsed -= mins;
    elapsed /= 60;

    int hours = elapsed % 100;

    print_digit(tmp, hours / 10, 0, 0);
    print_digit(tmp, hours % 10, 0, 16);

    print_digit(tmp, mins / 10, 40, 0);
    print_digit(tmp, mins % 10, 40, 16);

    print_digit(tmp, secs / 10, 80, 0);
    print_digit(tmp, secs % 10, 80, 16);

    oled_set_cursor(0, 0);
    oled_write_raw(tmp, 512);
}
